package com.demo.collection;

public class Emp {

	String name;
	int eid;
	
	
		public Emp(String name, int eid)
		{
		this.name=name;
		this.eid=eid;
	
	}


		public String getName() {
			return name;
		}


		public void setName(String name) {
			this.name = name;
		}


		public int getEid() {
			return eid;
		}


		public void setEid(int eid) {
			this.eid = eid;
		}
	
	
		
		
	}

